function fget(n, f)
  if not f then
    return gff[n] or 0
  end
  local r = gff[n] or 0
  return (bit.band(r, bit.lshift(1, f)) ~= 0)
end

function fset(n, f, v)
  if v == nil then
    gff[n] = f or 0
    return
  end
  if v then
    gff[n] = bit.bor(fget(n), bit.lshift(1, f))
  else
    gff[n] = bit.band(fget(n), bit.bnot(bit.lshift(1, f)))
  end
end

