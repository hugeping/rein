function collision(x,y)
	local xx,yy=flr(x/8),flr(y/8)
	c=mm(xx,yy)
	if (c==0) then return end
	-- TODO
	if sget((c%16)*8+x%8,
		flr(c/16)*8+(y%8))==0 then
		return
	end
	if fget(c,0) then
		local c,rc=mm(xx,yy)
		add(crashes,{rc,xx,yy})
		mm(xx,yy,0)
		mkexp(xx*8+4,yy*8+4)
	end
	return c
end

