function cld(r,x,y)
  local r1,r2=r,r
  local c={x=x,y=y,{0,0,r,0}}
  local x2=0
  x,y=0,0
  for i=1,rnd(2)+2 do
    x=x+r1
    x2=x2-r2
    if i==1 then
      r1=r1*(0.8+rnd(0.2))
      r2=r2*(0.8+rnd(0.2))
    else
      r1=r1*(0.5+rnd(0.5))
      r2=r2*(0.5+rnd(0.5))
    end
    add(c,{x,y,r1,rnd(1)})
    add(c,{x2,y,r2,rnd(1)})
  end
  c.d=(flr(rnd(2))==1) and 1 or -1
  c.f=(flr(rnd(dif(2,3,4)))==1) and true or false
  if (c.f and c.y<(r+8)) then c.y=r+8 end
  if c.f and abs(xnorm(c.x-plane.x)) < 128 then
    c.f=false
  end
  return c
end

