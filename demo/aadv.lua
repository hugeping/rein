			p.turn=p.turn+1
			if p.turn>#turn then
				p.turn=false
			else
				p.dir=turn[p.turn]
			end
		end
	end
	if (p.repair and p.t==0 and not p.crash) then
		if p.land then
			if (p.x<128 and p.x>-128 and friends>0) then
				ending=1
				friends=0
