	if p.sink then
		p.y=p.y+0.1
		if p.sink>6 then
			p.sink=false
		end
	end
	local o=pspr[abs(p.dir)]
	local a=o[4]
	if d<0 then a=0.5-a end
	local t=p.t^0.5
	if t ~= t then t = 0 end -- check nan
	if abs(p.dir)==1 then t=1 end
	if abs(p.dir)==2 and t<0.5 then t=0.5 end
	if p.turn then
		t=t*tturn[p.turn]
	end
